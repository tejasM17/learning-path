l =[2,3,1,9,5,8,6,7,4]

print(l)
l.sort() #  asending order
print(l)     #output will be in asending order
l.reverse() # reverse the list
print(l)   # output will be in reverse order
l.sort(reverse=True)  # desending order
print(l)   # output will be in desending order


